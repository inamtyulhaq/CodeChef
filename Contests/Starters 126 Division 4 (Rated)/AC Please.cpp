// Explanation:
// If temperature is greater than 30, output YES. Else output NO.

// Solution:
#include <bits/stdc++.h>
using namespace std;

int main() {
	int T;
	cin >> T;
	if(T>30)
	{
	    cout << "YES" << endl;
	}
	else
    {
        cout << "NO" << endl;
    }
    return 0;
}
