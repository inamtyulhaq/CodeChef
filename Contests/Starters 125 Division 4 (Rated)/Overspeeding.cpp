// Explanation:
// If the speed is less than 40 (40 is inclusive), output "NO" else output "YES".

// Solution:
#include <bits/stdc++.h>
using namespace std;

int main() {
	int S;
	cin >> S;
	if(S<=40)
	{
	    cout << "NO" << endl;
	}
	else
	{
	    cout << "YES" << endl;
	}
    return 0;
}
