// Question:
// https://www.codechef.com/problems/BIRYANI

// Explanation:
// Simply input two integers and cout their product.

// Solution:
#include <bits/stdc++.h>
using namespace std;

int main() {
	int T, X, Y;
	cin >> T;
	while(T--)
	{
	    cin >> X >> Y;
	    cout << X*Y << endl;
	}
	return 0;
}
