// https://www.codechef.com/problems/REACHTARGET

// Simply subtract current scores from the target given.

#include <bits/stdc++.h>
using namespace std;

int main() {
	int T;
	cin >> T;
	while(T--)
	{
	    int X, Y;
	    cin >> X >> Y;
	    int Z = X - Y;
	    cout << Z << endl;
	}
    return 0;
}
